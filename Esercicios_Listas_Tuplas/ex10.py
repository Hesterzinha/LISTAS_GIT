# Dada uma lista de palavras, junte todas elas em uma string separada por vírgulas.
# join() é um método de string que junta os elementos de uma lista em uma única string, usando o separador especificado (neste caso, ', ').

lista = ['maçã', 'banana', 'laranja', 'uva']

string = ', ' .join(lista)
print(string)  # Saída: 'maçã, banana, laranja, uva'
